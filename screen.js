// Parameter.
let canvasWidth = window.innerWidth;
let canvasHeight = window.innerHeight;
let minBorder = 40;

// Setup canvas.
let canvas = document.getElementById("screen");
canvas.setAttribute('width', canvasWidth);
canvas.setAttribute('height', canvasHeight);

// Substract minimal border.
let widthTileSpace = canvasWidth - 2*minBorder;
let heightTileSpace = canvasHeight - 2*minBorder;

// Calculate tile size.
let tileWidth = Math.floor(widthTileSpace / 9)
let tileHeight = Math.floor(heightTileSpace / 5)

// Calculate tile space.
widthTileSpace = 9 * tileWidth;
heightTileSpace = 5 * tileHeight;

// Calculate real border.
let bWidth = Math.floor((canvasWidth - widthTileSpace) / 2);
let bHeight = Math.floor((canvasHeight - heightTileSpace) / 2);

// Setup context.
var ctx = canvas.getContext('2d');
ctx.translate(0.5, 0.5)
ctx.strokeStyle = '#ffffff'
ctx.lineWidth = 1;

// Draw initial raster.
drawRaster(9,5);

// Start animation.
let animation;
let a = 0;
let frame = 0;
window.setInterval(startAnimation, 5000);

function startAnimation() {
    frame = 0;

    if(a > Object.keys(animations).length-1) {
        a = 0;
    }

    //alert(Object.keys(animations).length);

    
    animation = Object.keys(animations)[a];
    requestAnimationFrame(animate);

    a++;

}


function animate(){

    clearCanvas();
    drawRaster(9,5);
    drawFrame(animation,frame);

    sleep(50);

    if(frame < animations[animation].length-1) {
        
        frame++;
        requestAnimationFrame(animate);
    }
}

function drawFrame(animation, frame) {
    
    // Draw frames.
    for(let x=0;x<9;x++) {
    
        for(let y=0;y<5;y++) {

            //console.log(frame, y, x);

            let color = animations[animation][frame][y][x];
    
            if(color != 0.00) {
    
                xstart = bWidth + (x * tileWidth);
                ystart = bHeight + (y * tileHeight);
    
                ctx.fillStyle = 'rgba(255,255,255,'+color+')';
                ctx.fillRect(xstart, ystart, tileWidth, tileHeight);
            }
        }
    }
}

// Draw raster.
function drawRaster(xtiles,ytiles) {

    for(let x=0;x<xtiles+1;x++) {

        for(let y=0;y<ytiles+1;y++) {
    
            xstart = bWidth + (x * tileWidth);
            ystart = bHeight + (y * tileHeight);
    
            ctx.beginPath();

            // Horizontal.
            ctx.moveTo(xstart-5, ystart);
            ctx.lineTo(xstart+5, ystart);
    
            // Vertical.
            ctx.moveTo(xstart, ystart-5);
            ctx.lineTo(xstart, ystart+5);
            
            ctx.stroke();
    
            // Dot.
            var grd = ctx.createRadialGradient(xstart, ystart, 0, xstart, ystart, 5);
            grd.addColorStop(0, '#ffffff');
            grd.addColorStop(1, 'transparent');
            ctx.fillStyle = grd;
            ctx.fillRect(0,0,xstart+8,ystart+8);
        }
    }
}

// Clear canvas helper.
function clearCanvas() {

    ctx.save();

    // Use the identity matrix while clearing the canvas
    ctx.setTransform(1, 0, 0, 1, 0, 0);
    ctx.clearRect(0, 0, canvasWidth, canvasHeight);

    // Restore the transform
    ctx.restore();
}

// Sleep helper.
function sleep(milliseconds) {
    const date = Date.now();
    let currentDate = null;
    do {
      currentDate = Date.now();
    } while (currentDate - date < milliseconds);
  }

